    private void saveData(boolean flag) {
        try {
            
            Connection conn = DriverManager.getConnection(url, user, password);
            String sql = "";
            if(flag == true) {
                for(int i=0;i<listSV.size();i++) {
                    if(listSV.get(i).getId().equals(txtId.getText())) {
                        JOptionPane.showMessageDialog(this, "ID is Duplicate");
                        return;
                    }
                }
                sql = "INSERT INTO STUDENTS VALUES (?,?,?,?,?,?,?)";
                PreparedStatement st = conn.prepareStatement(sql);
                st.setString(1, txtId.getText());
                st.setString(2, txtName.getText());
                st.setString(3, txtEmail.getText());
                st.setString(4, txtPhone.getText());
                st.setBoolean(5,cboMale.isSelected()==true?true:false);
                st.setString(6, txtAddress.getText());
                st.setString(7, path);
                JOptionPane.showMessageDialog(this, "Add new Student Succsessfully !!");
                st.executeUpdate();
                loadDataToArr();
                loadDataToTable();
            } else {
                sql = "UPDATE STUDENTS SET Name = ?, Email = ?, PhoneNumber = ?,Gender=?, AddressStudent = ?, ImageStudent = ? "
                        + "WHERE IdStudent = ?";
                PreparedStatement st = conn.prepareStatement(sql);
                st.setString(1, txtName.getText());
                st.setString(2, txtEmail.getText());
                st.setString(3, txtPhone.getText());
                st.setBoolean(4,cboMale.isSelected()==true?true:false);
                st.setString(5, txtAddress.getText());
                st.setString(6, path);
                st.setString(7, txtId.getText());
                JOptionPane.showMessageDialog(this, "Student " + txtId.getText() +" has been update !!");
                st.executeUpdate();
                loadDataToArr();
                loadDataToTable();
            }
            conn.close();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

