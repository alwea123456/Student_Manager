    public boolean catch_bug() {
        StringBuilder sb = new StringBuilder();
       
        if (txtId.getText().equals("")) {
            sb.append("ID null");
        }
        
        if (txtName.getText().equals("")) {
            sb.append("\nName is null");
        }
        if (txtEmail.getText().equals("")) {
            sb.append("\nEmail is null");
        }else if (!(txtEmail.getText().matches("\\w+@\\w+(\\.\\w+){1,2}"))) {
            sb.append("\nEmail invalid");
        }
        if (txtPhone.getText().equals("")) {
            sb.append("\nPhone number is null");
        
        }
        if (!(cboMale.isSelected()) && !(cboFemale.isSelected())) {
            sb.append("\nChoose gender");
        }
        
        if (sb.length()>0) {
            JOptionPane.showMessageDialog(this, sb.toString());
            return false;
        }
        return true;
    }
    
