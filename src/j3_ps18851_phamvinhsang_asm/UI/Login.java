    public void Login() {
        String user = txtUser.getText();
        String pass = txtPassword.getText();
        flag = false;
        for(Account i: ListAcc) {
            if(user.equals(i.getUser_name())&&pass.equals(i.getPassword())) {
                JOptionPane.showMessageDialog(this, "Logged in sucsessfully");
                new Main(i.getRole()).setVisible(true);
                flag = true;
                this.dispose();
                break;
                
            }
        }
        if(flag==false) {
            JOptionPane.showMessageDialog(this, "User name or password Incorrect!");
        }
    }

    private void create() {
        Error_manage();
        try {
            
            java.sql.Connection conn = DriverManager.getConnection(url, userName, password);
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String sql = "INSERT INTO USERS VALUES (?,?,?)";
            PreparedStatement st = conn.prepareStatement(sql);
            st.setString(1, txtUser_su.getText());
            st.setString(2, txtPassWord_su.getText());
            st.setInt(3, cboRole.getSelectedItem()=="Student"?0:1);
            st.executeUpdate();
            conn.close();
            st.close();
            JOptionPane.showMessageDialog(this, "Your account has been created");
            load_acc();
        } catch(Exception ex) {
            ex.printStackTrace();
        }
    }
    
    public void load_acc() {
        ListAcc.clear();
        try {
            java.sql.Connection conn = DriverManager.getConnection(url, userName, password);
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            java.sql.Statement st = conn.createStatement();
            String sql = "SELECT * FROM USERS";
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()) {
                Account acc = new Account(rs.getString(1), rs.getString(2), rs.getInt(3));
                ListAcc.add(acc);
            }
            System.out.println(ListAcc.size());
            rs.close();
            st.close();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
    void up_down_textField(JLabel label,JTextField textField, Boolean flag) {
        // maxY là ở trạng thái bình thường
        int maxY = label.getY();
        int minY = label.getY()-25;
        if(textField.getText().equalsIgnoreCase("")==false&&label.getY()==maxY) {
            System.out.println("ok");
        } else {
            Timer timer = new Timer(10, this);
            timer.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent arg0) {
                    if(flag == false) {
                        if(label.getY()<=minY) {
                            timer.stop();
                        }
                        label.setLocation(label.getX()-5,label.getY()+5);
                        textField.setBorder(BorderFactory.createMatteBorder(0, 0, 1, 0, Color.WHITE));
                    }
                    if(flag == true) {
                        System.out.println(label.getY());
                        if(label.getY()==minY) 
                            timer.stop();
                        if(label.getY()>=maxY) {
                            label.setLocation(lblUser.getX()+5, lblUser.getY()-5);  
                            label.setBackground(new Color(58,111,254));
                            textField.setBorder(BorderFactory.createMatteBorder(2, 2, 2, 2, Color.WHITE));
                        }
                    }
                }
            });
            timer.start();
        }
    }
    
