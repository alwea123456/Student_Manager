    void Error_manage() {
        if(txtUser_su.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "User name invalid");
        }
        for(Account i : ListAcc) {
            if(i.getUser_name().equals(txtUser_su.getText())) {
                JOptionPane.showMessageDialog(this, "User name is already taken");
                return;
            } 
            
        }
        if(txtPassWord_su.getText().equalsIgnoreCase(txtConfirm_su.getText())==false) {
            JOptionPane.showMessageDialog(this, "2 Password is not match");
            return;
        }
        if(txtPassWord_su.getText().length()<=6) {
            JOptionPane.showMessageDialog(this, "Password must be more than 6 characters");
            return;
        }
    }
    
