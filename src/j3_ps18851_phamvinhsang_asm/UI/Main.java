    private void StdBarChart() {
        double weak = 0;
        double average = 0;
        double good = 0;
        double veryGood = 0;
        double excellent = 0;
        for(int i =0;i<listScore.size();i++) {
            if(listScore.get(i).getAverage()<4) {
                weak++;
            } else if(listScore.get(i).getAverage()<6.5) {
                average++;
            } else if(listScore.get(i).getAverage()<8) {
                good++;
            } else if(listScore.get(i).getAverage()<9) {
                veryGood++;
            } else if(listScore.get(i).getAverage()<=10) {
                excellent++;
            }
        }
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        dataset.setValue(weak, "marks", "Weak");
        dataset.setValue(average, "marks", "average");
        dataset.setValue(good, "marks", "good");
        dataset.setValue(veryGood, "marks", "veryGood");
        dataset.setValue(excellent, "marks", "excellent");
    
        JFreeChart chart = ChartFactory.createBarChart3D("Student Score","Category","Score",dataset,
                PlotOrientation.HORIZONTAL, rootPaneCheckingEnabled, rootPaneCheckingEnabled, rootPaneCheckingEnabled);
        CategoryPlot p = chart.getCategoryPlot();
        p.setRangeGridlinePaint(Color.BLUE);
        ChartPanel panel = new ChartPanel(chart);
        panel.setVisible(true);
        panel.setSize(panel_Count.getWidth(), 300);
        panel_Count.add(panel);
        
    }
    
    private void stdPiechart() {
        double weak = 0;
        double average = 0;
        double good = 0;
        double veryGood = 0;
        double excellent = 0;
        double sum = listScore.size();
        for(int i =0;i<listScore.size();i++) {
            if(listScore.get(i).getAverage()<4) {
                weak++;
            } else if(listScore.get(i).getAverage()<6.5) {
                average++;
            } else if(listScore.get(i).getAverage()<8) {
                good++;
            } else if(listScore.get(i).getAverage()<9) {
                veryGood++;
            } else if(listScore.get(i).getAverage()<=10) {
                excellent++;
            }
        }
        weak = weak/sum*100;
        average = average/sum*100;
        good = good/sum*100;
        veryGood = veryGood/sum*100;
        excellent = 100-(weak+average+good+veryGood);
        DefaultPieDataset dataset = new DefaultPieDataset();
        dataset.setValue("weak", weak);
        dataset.setValue("average", average);
        dataset.setValue("good", good);
        dataset.setValue("verygood", veryGood);
        dataset.setValue("excellent", excellent);
        JFreeChart chart = ChartFactory.createPieChart3D("learning rate", dataset, true, true, true);
        PiePlot p = (PiePlot)chart.getPlot();
        ChartPanel panel = new ChartPanel(chart);
        panel.setSize(panel_Pass.getWidth(),panel_Pass.getHeight());
        panel_Pass.add(panel);
        
    }
    
