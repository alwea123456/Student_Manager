    private void SendEmail() {
        String APP_EMAIL = txtEmail.getText().trim();
        String APP_PASSWORD = txtPassword.getText();
        String RECEIVE = txtReceiver.getText();
        try {
            Properties p = new Properties();
                p.put("mail.smtp.auth","true");
                p.put("mail.smtp.host", "smtp.gmail.com");
                p.put("mail.smtp.starttls.enable", "true");
                p.put("mail.smtp.port", 587);

                Session session = Session.getDefaultInstance(p, new javax.mail.Authenticator() {
                    protected PasswordAuthentication getPasswordAuthentication() {
                        return new PasswordAuthentication(APP_EMAIL, APP_PASSWORD);
                    }
                });
                MimeMessage message = new MimeMessage(session);
                if(cboSendStyle.getSelectedItem().equals("CC")) {
                    message.setRecipients(Message.RecipientType.CC, InternetAddress.parse(RECEIVE));
                } else 
                    message.setRecipients(Message.RecipientType.BCC, InternetAddress.parse(RECEIVE));
                message.setSubject(txtSubject.getText());
                
                if(path.equals("")) {
                    message.setSubject(txtSubject.getText());
                    message.setText(txtMessage.getText());

                    Transport.send(message);
                } else {
                    MimeBodyPart bodypartText = new MimeBodyPart();
                    bodypartText.setText(txtMessage.getText());

                    MimeBodyPart bodyPartFile = new MimeBodyPart();
                    DataSource source = new FileDataSource(path);

                    bodyPartFile.setDataHandler(new DataHandler(source));
                    bodyPartFile.setFileName(txtFile.getText());

                    Multipart multipart = new MimeMultipart();
                    multipart.addBodyPart(bodypartText);
                    multipart.addBodyPart(bodyPartFile);

                    message.setContent(multipart);
                    Transport.send(message);
                    
                }
                JOptionPane.showMessageDialog(this, "Send Successfully");
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

