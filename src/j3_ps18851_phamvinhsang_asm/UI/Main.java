    private void btnTop3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTop3MouseClicked
        // TODO add your handling code here:
        listScore.clear();
        DefaultTableModel model = (DefaultTableModel)tblScore.getModel();
        model.setRowCount(0);
        try {
            Connection conn = DriverManager.getConnection(url, user, password);
            Statement st = conn.createStatement();
            String sql = "SELECT TOP 3 GRADE.*, Name FROM GRADE JOIN STUDENTS ON GRADE.Idstudent = STUDENTS.IdStudent \n" +
            "ORDER BY (English + Informatics + physical) DESC";
            ResultSet rs = st.executeQuery(sql);
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            while(rs.next()) {
                Grade g= new Grade();
                g.setIDStudent(rs.getString("IdStudent"));
                g.setName(rs.getString("Name"));
                g.setEnglish(rs.getDouble("English"));
                g.setInformatic(rs.getDouble("Informatics"));
                g.setPhysics(rs.getDouble("Physical"));
                g.setAverage();
                listScore.add(g);
            }
            for(Grade i: listScore){
                Object[] obj = {i.getIDStudent(), i.getName(),
                    i.getEnglish(), i.getInformatic(), i.getPhysics(), df.format(i.getAverage())};
                model.addRow(obj);
            }
            tblScore.setModel(model);
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnTop3MouseClicked

